package org.micro.kojanni.string_search.kmp_lesson;

import lombok.experimental.UtilityClass;

/**
 * Что такое префикс-функция:
 * Для строки s[0..n-1] префикс-функция π[i] — это длина наибольшего собственного префикса подстроки s[0..i], который одновременно является её суффиксом.
 * Собственный означает, что префикс не может равняться всей подстроке (кроме особого случая π[0] = 0).
 *
 * Пример для "ABABC":
 * π[0] = 0 (всегда)
 * π[1] для "AB": префиксы "A", суффиксы "B" — нет совпадения → 0.
 * π[2] для "ABA": префиксы "A", "AB"; суффиксы "A", "BA" — общий "A" длины 1 → π[2]=1.
 * π[3] для "ABAB": общий "AB" длины 2 → π[3]=2.
 * π[4] для "ABABC": нет общего префикса-суффикса → π[4]=0.
 *
 * Итог: π = [0, 0, 1, 2, 0].
 */
@UtilityClass
public class PrefixFunction {

    /**
     * Медленная префикс-функция:
     * сравниваем подстроки s[0..k-1] и s[i-k+1..i]:
     * Для каждой позиции i (от 1 до n-1) мы перебираем все возможные длины k от i вниз до 1
     * и проверяем, равны ли первые k символов последним k символам подстроки s[0..i]
     * Первое совпадение даёт значение π[i].
     */
    static int[] slowPrefixFunction(String s) {
        int n = s.length();
        int[] pi = new int[n];
        for (int i = 1; i < n; i++) {
            // Перебираем возможные длины префикса от i вниз
            for (int k = i; k > 0; k--) {
                if (s.substring(0, k).equals(s.substring(i - k + 1, i + 1))) {
                    pi[i] = k;
                    break;
                }
            }
        }
        return pi;
    }

    /**
     * Быстрая префикс-функция:
     * использовать ранее вычисленные значения π, чтобы не перебирать все длины,
     * а сразу переходить к подходящему кандидату
     *
     * Если мы знаем π[i-1] = j, то для позиции i мы хотим проверить,
     * можно ли расширить предыдущий префикс.     *
     * Новый символ s[i] сравнивается с символом s[j] (следующим после префикса длины j).
     * Если они равны, то π[i] = j + 1.     *
     * Если не равны, нужно взять меньший префикс — длиной π[j-1]
     * (т.е. длину наибольшего префикса, который является суффиксом предыдущего префикса).
     * И так повторять, пока не найдём совпадение или не дойдём до нуля.
     */
    static int[] fastPrefixFunction(String s) {
        int n = s.length();
        int[] pi = new int[n];
        for (int i = 1; i < n; i++) {
            int j = pi[i - 1];
            while (j > 0 && s.charAt(i) != s.charAt(j)) {
                j = pi[j - 1];
            }
            if (s.charAt(i) == s.charAt(j)) {
                j++;
            }
            pi[i] = j;
        }
        return pi;
    }
}
